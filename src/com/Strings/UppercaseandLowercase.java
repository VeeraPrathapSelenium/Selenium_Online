package com.Strings;

public class UppercaseandLowercase {

	public static void main(String[] args) {
		
		String name="RaMyA";
		
		//Upper case
		
		//Syntax:-Stringvariable.toUppercase();
		//this method will always return a string so store it in a string variable
		
		String upvariable=name.toUpperCase();
		System.out.println(upvariable);
		
	//Lower case
		
		//Syntax:-Stringvariable.toLowercase();
		//this method will always return a string so store it in a string variable
		
		String lowvariable=name.toLowerCase();
		System.out.println(lowvariable);
		
		
		
		
	}
	
	
}
